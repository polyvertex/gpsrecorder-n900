#include "stable.h"


//---------------------------------------------------------------------------
// ExporterSink
//---------------------------------------------------------------------------
ExporterSink::ExporterSink (Exporter* pParent)
: QObject(pParent)
{
  Q_ASSERT(pParent);

  m_pFile = 0;
}

//---------------------------------------------------------------------------
// ~ExporterSink
//---------------------------------------------------------------------------
ExporterSink::~ExporterSink (void)
{
}



//---------------------------------------------------------------------------
// close
//---------------------------------------------------------------------------
void ExporterSink::close (void)
{
  if (m_pFile)
  {
    fclose(m_pFile);
    m_pFile = 0;
  }

  m_strFilePath.clear();
}
